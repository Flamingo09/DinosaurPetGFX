public class Dinosaur {
	int age;
	int happy;
	int hunger;
	String name;
	
	public Dinosaur(int age, int happy, int hunger, String name) {
		this.age = age;
		this.happy = happy;
		this.hunger = hunger;
		this.name = name;
	}
	
	public int getAge() {
		return age;
	}
	
	public int getHappy() {
		return happy;
	}
	
	public int getHunger() {
		return hunger;
	}
	
	public String getName() {
		return name;
	}
	
	
}
